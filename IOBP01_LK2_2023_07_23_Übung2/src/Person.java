public class Person {
	private String name;
	private boolean vip = false;
	private int age;
	
	public void setName (String n) {
		name = n;
		}
	public String getName () {
		return name;
	}
	public void setVip (boolean v) {
		vip = v;
	}
	public void setAge (int a) {
		age = a;
	}
	public boolean getVip () {
		return vip;
	}
	public int getAge () {
		return age;
	}
	
	
	
	
}
